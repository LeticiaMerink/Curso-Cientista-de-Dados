# BANCO DE DADOS são repositórios sistêmicos de informação que buscam resolver problemas de gerenciamento da informação.

# * Trabalho em equipe -> banco de dados permitem que várias pessoas trabalhem com os mesmos dados na mesma hora (uma  equipe)
# * Segurança de acesso -> nos bancos de dados é possivel configurar quem te acesso a qual tipo de conteúdo e é possível ver 
# quem fez o que no banco de dados
# * Escalabilidade -> o banco de dados se propõe a resolver esse problema de escabilidade porque nos podemos armazenar muitos
# dados e fazer também consultas a grandes conjuntos de dados.
# * Integridade -> é possivel criar regras de integridade de informação, ou seja, a gente pode impedir que um determinado 
# dado seja cadastrado se aquele não for o tipo correto de cadastro, podemos impedir duplicidade de informação por exemplo.
# * Padronização de acesso -> pra acessar os dados você não precisa acessar arquivos (excel, word, outros) que estão 
# espalhados , ou seja, os dados são guardados todos no banco de dados.

# Os bancos de dados de modo geral possuem 3 principais componentes (!- dados, 2- SGBD e uma 3- linguagem de consulta)
# 1- Base de dados -> são os dados em si, são as tabelas (ou informações) que estão armazenadas nesse banco.
# 2- SGBD (Sistema Gerenciados Banco de Dados) -> sistema que gerencia os arquivos do banco/intermedia o acesso ao banco.
# 3- Linguagem de exploração -> linguagem para executarmos consulta, exploração e alteração nos dados.


